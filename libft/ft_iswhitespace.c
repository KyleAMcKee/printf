#include "libft.h"

int	ft_iswhitespace(const char c)
{
	if (c == ' ' || c == '\n'
		|| c == '\t' || c == '\r'
		|| c == '\v' || c == '\f')
	{
		return (1);
	}
	else
		return (0);
}
